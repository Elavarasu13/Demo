package Strings;

public class StringLiterals {

	public static void main(String[] args) 
	{
		//StringObject//
		String s1 =  new String("");
		//StringLiteral//
		String s2 = "Dhruv";
		//output//
		System.out.println(s1+ "_" +s2);
		System.out.println(s1.length());
		System.out.println(s1.isEmpty());
		System.out.println(s1.isBlank());
	}

}
